    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        String chassi;
        String modelo;
        String fabricante;
        String cor;
        String ano;
        String preco;
        
        chassi = txtChassi.getText();
        modelo = txtModelo.getText();
        fabricante = txtFabricante.getText();
        cor = txtCor.getText();
        ano = txtAno.getText();
        preco = txtPreco.getText();
        
        VeiculoDTO objveiculoDTO= new VeiculoDTO();
        objveiculoDTO.setChassi(chassi);
        objveiculoDTO.setModelo(modelo);
        objveiculoDTO.setFabricante(fabricante);
        objveiculoDTO.setCor(cor);
        objveiculoDTO.setAno(ano);
        objveiculoDTO.setPreco(preco);
        
        VeiculoDAO objveiculoDAO = new VeiculoDAO();
        objveiculoDAO.cadastrarVeiculo(objveiculoDTO);
    }//GEN-LAST:event_btnCadastrarActionPerformed

